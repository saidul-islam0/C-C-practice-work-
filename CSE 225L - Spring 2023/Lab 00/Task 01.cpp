/*
Take 2 integer inputs from keyboard and print their sum.
*/

#include <iostream>

using namespace std;

int main()
{
    int number1, number2;

    cout << "Enter First Number: ";
    cin >> number1;

    cout << "Enter Second Number: ";
    cin >> number2;

    cout << endl << "Sum is: " << (number1 + number2) << endl;

    return 0;
}
